#include <iostream>


using namespace std;

int main () {
	int numA;
	int numB;
	
	cout << "digite um numero A: ", cin >> numA;
	cout << "digite um numero B: ", cin >> numB;
	
if (numA / numB) {
	
	cout << "o numero A é divisivel pelo numero B ";
	
} else {
	cout << "o numero A não é divisivel pelo numero B ";
}
	


return 0; 
	
}